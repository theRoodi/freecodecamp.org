//Selecting from Many Options with Switch Statements

function caseInSwitch(val) {
    var answer = "";
    // Only change code below this line
  
    switch(val) {
      case 1:
        answer = 'alpha'
        console.log(answer);
        break;
      case 2:
        answer = 'beta'
        console.log(answer);
        break;
      case 3:
        answer = 'gamma'
        console.log(answer);
        break;
      case 4:
        answer = 'delta'
        console.log(answer);
        break;
    }
  
    // Only change code above this line
    return answer;
  }
  
  caseInSwitch(1);